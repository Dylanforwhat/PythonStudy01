#匹配邮箱地址，只允许qq，163，gmail三种邮箱
#{内容}@{内容}.{内容}
r = r'(^[\w-]+(\.[\w-]+)*@(qq)(\.[\w-]+)+$)'
# +表示这个包含-的全字符合计至少出现一个字母
# \.表示点本身
s = 'a.v.c@qq.com.z.a.s.f'
print(re.findall(r,s))
